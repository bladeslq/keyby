import Groq from 'groq-sdk'

const groq = new Groq({ apiKey: process.env.GROQ_API_KEY })

export async function parseMessage(text) {
  const response = await groq.chat.completions.create({
    model: 'llama-3.3-70b-versatile',
    max_tokens: 1024,
    messages: [{
      role: 'user',
      content: `Ты парсер объявлений об аренде недвижимости из WhatsApp-чатов риелторов Казани. Цены всегда в рублях/месяц.

## Сокращения
- 1к, 2к, 3к, 4к = количество комнат (1-комнатная и т.д.)
- ст = студия
- БД = без детей
- БЖ / БЖВ = без животных
- нашим / только нашим / гражданам = только гражданам РФ
- ЧП / ЦП / хозяин = собственник (без посредников)
- п/п = после предыдущих (подождать)
- к/с = коммунальные услуги включены / не включены
- дп = депозит
- пл / м / м² = площадь в квадратных метрах
- эт = этаж
- ПАО = парковочное место

## Районы Казани и ориентиры
- Вахитовский: центр, ЦП, Кремль, Баумана, Черное озеро, Федосеевская, Островского, Карла Маркса, Университетская, Муштари
- Ново-Савиновский: Савиново, Чистопольская, Сибирский тракт, Амирхана, Ямашева, ЖК Светлая Долина, ЖК Весна, ЖК Лебяжье
- Московский: Горки, пр. Победы, Зорге, Юдино, ЖК Седьмое Небо, ЖК Московский
- Авиастроительный: Авиастроительная, Тэцевская, ЖК Весна, ЖК Сокол
- Приволжский: Азино, ул. Минская, Закамская, Дальняя, ЖК Столичный, ЖК Казань XXI век
- Советский: Танкодром, Дербышки, ул. Дубравная, Маяковского, ЖК Симфония, ЖК Арт Сити
- Кировский: Левченко, Аракчино, ЖК Берег
- Салават Купере: одноимённый район, новые дома ГЖФ
- Академический: рядом с КФУ, ул. Академика Арбузова
- Компрессорный: пос. Компрессорный

## Правила извлечения
- Тип (type): room / studio / 1k / 2k / 3k / 4k+ / house / other
- Площадь: число без единиц (35м → 35, "площадь 50" → 50)
- Цена: только цифры в рублях, без знаков
- Депозит: если "залог 1 месяц" или "дп 1м" → умножь на цену аренды
- Адрес: ЖК + улица + номер дома если есть, иначе только ЖК или улицу
- Район: определяй по ЖК, улице, метро, ориентиру — даже если не указан явно
- description: условия (БД, БЖ, только нашим, коммуналка, парковка и т.д.)

Если это НЕ объявление об аренде (приветствие, вопрос, флуд, поиск жилья) — верни: null

Если объявление — верни ТОЛЬКО JSON без пояснений:
{
  "title": "краткое название: тип + ЖК или улица",
  "type": "тип объекта",
  "district": "район Казани или null",
  "address": "адрес (ЖК/улица/дом) или null",
  "price": число или null,
  "deposit": число или null,
  "rooms": число или null,
  "area": число или null,
  "floor": число или null,
  "totalFloors": число или null,
  "description": "условия аренды одной строкой или null",
  "raw": "оригинал сообщения"
}

Сообщение:
${text}`,
    }],
  })

  const content = response.choices[0].message.content.trim()
  if (content === 'null') return null

  const match = content.match(/\{[\s\S]*\}/)
  if (!match) return null

  try {
    return JSON.parse(match[0])
  } catch {
    return null
  }
}
